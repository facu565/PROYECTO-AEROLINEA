    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String ruta = txtruta.getText();
        String contenido = area.getText();

        try {
            FileOutputStream archivo = new FileOutputStream(ruta + ".pdf");
            Document doc = new Document(PageSize.A7, 50, 50, 50, 50);
            PdfWriter.getInstance(doc, archivo);
            doc.open();
            doc.add(new Paragraph(contenido));

            doc.close();

            JOptionPane.showMessageDialog(null, "Pdf correctamente creado.");

        } catch (Exception e) {
            System.out.println("Error");
        }
        FileWriter fichero = null;
        PrintWriter pw = null;
        try{
            fichero = new FileWriter("./src/aerolineas/registro.txt",true);
            pw = new PrintWriter(fichero);
            pw.println(texto1+";"+texto2+";"+texto3+";"+texto4+";"+texto5+";"+"Hora Salida: 16hs;"+"Hora Llegada: 18 hs;"+codigo);
        } catch (IOException e) {
            e.printStackTrace();
        }finally{
            try{
                if (null!=fichero)
                fichero.close();}
            catch (Exception e2){
                e2.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

